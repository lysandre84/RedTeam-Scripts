# port_sweep.sh — balayage de ports dans un réseau
if [ $# -lt 1 ]; then
  echo "Usage: $0 --range <network>"
  exit 1
fi
while [[ "$#" -gt 0 ]]; do
  case $1 in
    --range) NETWORK="$2"; shift ;;
  esac
  shift
done

echo "[*] Balayage des hôtes dans $NETWORK"
nmap -sS -p- "$NETWORK"
